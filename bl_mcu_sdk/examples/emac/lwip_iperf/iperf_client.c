// clang-format on
static void my_iperf_netconn_client(void)
{
    struct netconn *conn;
    err_t err;

    ip4_addr_t remote_ipaddr;

    while (1) {
        conn = netconn_new(NETCONN_TCP);
        if (conn == NULL) {
            MSG("creat netconn failed!\r\n");
            return;
        }

        IP4_ADDR(&remote_ipaddr, SERVER_IP_ADDR0, SERVER_IP_ADDR1, SERVER_IP_ADDR2, SERVER_IP_ADDR3);

        err = netconn_connect(conn, &remote_ipaddr, IPERF_CLIENT_PORT);

        if (err == ERR_OK) {
            MSG("connet to server success!\r\n");

            while (1) {
                netconn_write(conn, send_buf, sizeof(send_buf), NETCONN_NOCOPY);
            }
        } else {
            netconn_close(conn);
            netconn_delete(conn);
            MSG("connect failed! err:%d\r\n", err);
        }
    }
}

