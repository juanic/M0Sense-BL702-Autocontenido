static void lwiperf_report(void *arg,
                           enum lwiperf_report_type report_type,
                           const ip_addr_t *local_addr,
                           u16_t local_port,
                           const ip_addr_t *remote_addr,
                           u16_t remote_port,
                           u32_t bytes_transferred,
                           u32_t ms_duration,
                           u32_t bandwidth_kbitpsec)
{
    LWIP_UNUSED_ARG(arg);
    LWIP_UNUSED_ARG(local_addr);
    LWIP_UNUSED_ARG(local_port);

    MSG("IPERF report: type=%d, remote: %s:%d, total bytes: %" U32_F
        ", duration in ms: %" U32_F ", kbits/s: %" U32_F "\n",
        (int)report_type, ipaddr_ntoa(remote_addr), (int)remote_port,
        bytes_transferred, ms_duration, bandwidth_kbitpsec);
}

/**
 * @brief
 *
 * @param arg
 */
static void iperf_client_thread(void *arg)
{
    uint8_t iptxt[20];
    ip4_addr_t iperf_ipaddr;
    IP4_ADDR(&iperf_ipaddr, SERVER_IP_ADDR0, SERVER_IP_ADDR1, SERVER_IP_ADDR2, SERVER_IP_ADDR3);

    lwiperf_start_tcp_client(&iperf_ipaddr, IPERF_CLIENT_PORT, LWIPERF_CLIENT, lwiperf_report, NULL);

    sprintf((char *)iptxt, "%s", ip4addr_ntoa(&iperf_ipaddr));
    MSG("lwiperf tcp client test IP address & port: %s:%d\r\n", iptxt, IPERF_CLIENT_PORT);

    while (1) {
        vTaskDelay(1);
    }
}

